                        <p class="para">
                        The Talang Satang National Park is a national park in Kuching Division, Sarawak, Malaysia. It is Sarawak's first marine protected area, and covers the four islands Pulau Talang-Talang Besar, Pulau Talang-Talang Kecil, Pulau Satang Besar and Pulau Satang Kecil and surrounding coral reefs.<br>

                        The park was established to protect marine turtles; four species of marine turtles are known to nest in the park, including green turtles and hawksbill turtles.<br>
                        Owing to their small size and fragile environment, the Talang Talang Islands have not been developed for tourism. Instead the government’s emphasis has been on turtle conservation.Sarawak is a pioneer of turtle conservation with Talang Talang being the first site in the world to be the focus of turtle research and management activities. The now common turtle conservation strategies of tagging adult turtles and relocating eggs to hatcheries were first tried out on the tiny islands of Talang Talang.Conservation work continues today. Park rangers monitor the beaches, record turtle landings, nests, number of eggs and hatchlings. There are turtle hatcheries on both of the islands. The largest hatchery is on Pulau Talang Besar.
                        <br>
                        Whilst turtles land on the island’s beaches throughout the year, the main turtle nesting season is from April to October.  June, July and August are the busiest months with 10-20 turtle landings per night.  2,000-2,500 nests and 150,000-200,000 eggs are recorded on Pulau Talang Besar each year.  On average 65% of eggs hatch.</p>
                                                                      
