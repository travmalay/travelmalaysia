                        <p class="para">
                        The Gunung Mulu National Park is a national park in Miri Division, Sarawak, Malaysia. It is a UNESCO World Heritage Site that encompasses caves and karst formations in a mountainous equatorial rainforest setting. The park is famous for its caves and the expeditions that have been mounted to explore them and their surrounding rainforest, most notably the Royal Geographical Society Expedition of 1977–1978, which saw over 100 scientists in the field for 15 months. This initiated a series of over 20 expeditions now named the Mulu Caves Project.

                        The national park is named after Mount Mulu, the second highest mountain in Sarawak.</p>
                                              
                        <br>
                        <ul class="list-group para">
                          <li class="list-group-item list-group-item-secondary">
                            <p>
                            <span>Climate </span><br><p>The climate at Mulu national park are affected by northeast monsoon (December to March) and southwest monsoon (May to October). The amount of rainfall is high, which ranges from 4,000 millimetres (160 in) to 5,000 millimetres (200 in). In the lowlands, the temperature ranges from 23 °C (73 °F) to 26 °C (79 °F). At the summit of Mount Mulu, the temperature ranges from 14 °C (57 °F) to 18 °C (64 °F). The climate data shown is for the Mulu village near the airport and the entrance of the park. Interior and higher parts of the park have a cooler and wetter climate.</p>
                            </p>
                           
                          </li>
                          <li class="list-group-item list-group-item-primary">
                            
                            <span>Biodiversity </span>
                            
                           
                          </li>
                          <li class="list-group-item list-group-item-secondary">
                            
                            <span>Fauna </span><br><p>There are 20,000 species of invertebrates, 81 species of mammals, 270 species of birds, 55 species of reptiles, 76 species of amphibians and 48 species of fish identified in the park area</p>
                            
                           
                          </li>
                          </li>
                          <li class="list-group-item list-group-item-secondary">
                            
                            <span>Flora </span><br><p>Gunung Mulu National Park contains a large number of plant species. The park has 17 vegetation zones, with 3,500 species of vascular plants, and 1,500 species of flowering plants. There are 109 species in 20 genera of palms, over 1,700 mosses and liverworts, 8,000 species of fungi, and 442 species of spore-producing pteridophytes are recorded. Examples of vegetation zones found in the park are: peat swamp forest, heath, mixed dipterocarp forest, moss forest, and montane ecosystems. Lowland forests occupies 40% of the park area while montane forests occupies 20% of the park area.</p>
                            
                           
                          </li>
                          </li>
                          <li class="list-group-item list-group-item-primary">
                            
                            <span>Trekking routes </span><br><p>The national park has three climbing treks: The Pinnacles Summit Trek, The Headhunter's Trail, and Gunung Mulu Summit Trek.</p>
                            
                           
                          </li>
                          </li>
                          <li class="list-group-item list-group-item-secondary">
                            
                            <span>The Pinnacles Summit Trek </span><br><p>Pinnacles Summit Trek is a 3 days/2 nights trek leading to the view of the Gunung Mulu Pinnacles. The trek is usually starts with a one or two hours boat ride from Melinau river to Kuala Berar (Berar delta). The Base Camp 5 is a 7.8 km walk from Kuala Berar. Camp 5 is located near the Melinau Gorge which separates Mount Benarat from Mount Api. After Camp 5, there is a 1.2 km trek rising 1,200 metres; passing from dipterocarp forest to moss forest. The last section requires ropes and ladders to help with the climb</p>
                            
                           
                          </li>
                          <li class="list-group-item list-group-item-secondary">
                            
                            <span>The Headhunter's Trail </span><br><p>The Headhunter's Trail is route taken to enter or leave the Gunung Mulu National Park. The trail follows the route taken by the Kayan from Melinau river to Melinau Gorge. The trail starts with a boat ride to Kuala Berar, then trek for two to three hours to reach Camp 5. From Camp 5, there is an 11.2 km trail leading to Kuala Terikan which could last for four to five hours. The climbers can either choose to stay at a ranger station Nanga Metawai (15 minutes from Kuala Terikan) or a longhouse named Rumah Bala Lesong (three to four hours by longboat). After that, a boat ride further downriver would reach Nanga Medamit where there is a road leading to Limbang.</p>
                            
                           
                          </li>
                          <li class="list-group-item list-group-item-secondary">
                            
                            <span>The Gunung Mulu Summit trek </span><br><p>The Gunung Mulu Summit trek is the only trek to reach the summit of Mount Mulu. The summit is located at 24 km away from the park headquarters. The trek starts from the national park headquarters to reach Camp 3. It is a 12 km hike through the primary forests with an elevation of 1,200 metres. The mossy forest starts from Camp 3. It will be a few hours to climb to reach Camp 4. After Camp 4, there are a few vertical climbs which requires knotted ropes which eventually leads to the summit. Camp 1 is located along the descending trek from the summit of Mount Mulu. There is another 3-hour hike to reach the park headquarters from Camp 1.</p>
                            
                           
                          </li>
                          <li class="list-group-item list-group-item-primary">
                            
                            <span>Management and facilities </span><br><p>The Protected Areas and Biodiversity Conservation (PABC) Unit of the Sarawak Forestry Corporation is responsible for the management Totally Protected Areas (TPA) of Gunung Mulu National Park which covers 90% of the park area and is closed to the public. The remaining 10% of the park area is opened to visitors and is managed by Borsarmulu Park Management Sdn Bhd.</p>
                            
                           
                          </li>
                          <li class="list-group-item list-group-item-primary">
                            
