                        <p class="para">
                        Batang Ai National Park (Malay: Taman Negara Batang Ai) is a national park located in Sri Aman Division, Sarawak, Malaysia. It is located in Lubok Antu, some 250 kilometers east of Kuching. The park covers an area of 24 square kilometres (9.3 sq mi) of extensive tropical rainforest with a number or rare and protected animals surrounding the 24 square kilometer artificial lake created by the Batang Ai hydroelectric reservoir. The park was proclaimed in 1991, and has become increasingly popular with locals and tourists despite the lack of facilities.

                        Access is possible by chartering a boat (traditional boat called Longboat), as water is the main method of transportation in the area. The ride takes approximately about 40 minutes to Batang Ai National Park headquarters. The lush dipterocarp forests are home to the orang-utan, gibbons, and hornbills. The lake creates a beautiful environment and gives a sense of peace and tranquillity. The local inhabitants are mostly Iban, and tours to nearby Iban longhouses are also a tourist diversion. This national park takes strong steps in having the local communities involved in its management. The communities have formed a cooperative called 'Kooperasi Serbaguna Ulu Batang Ai' and are helping to conserve the park.</p>
                                              
