                            *Two shows from Mondays to Thursdays at 8.30pm-8.45pm and 9.30pm-9.45pm. There are three shows from Fridays to Sundays at 8.30pm-8.45pm, 9.30pm-9.45pm and 10.30pm-10.45pm. Shows may be limited during the Muslim fasting month so do check the timing once you are here.</p>
