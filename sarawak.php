                        <p class="para">
                        Santubong National Park is a great place for a day visit. Santubong is located on the Damai Peninsula, 35km north of Kuching. Seen from sea level, it’s irregular rainforest-covered peaks rise steeply to 810m, forming a catchment for a variety of wildlife. Gunung Santubong provides a spectacular backdrop to hotel resorts, local fishing villages, and the Sarawak Cultural Village. The main popular activities here are hiking to Santubong Summit and swimming at the Waterfall. Its striking peak is visible from Kuching on most days. During your visit, make sure you ask a local about the legend of the Santubong princess!</p>
                                              
