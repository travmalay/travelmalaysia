                        <p class="para">
                        A National Park since 1957, Bako offers the perfect introduction to Sarawak’s forests and wildlife.  The park covers the northern tip of the Muara Tebas peninsula, an area of 27 sq km.  Despite its seemingly small size, Bako contains a wide range of vegetation – swamp forest, scrub-like padang vegetation, mangrove forest, dipterocarp forest, delicate cliff vegetation and more.  In fact, at Bako it is possible to see almost every type of vegetation found in Borneo. Bako also contains a rich variety of wildlife and a coastline covered with small bays, coves and beaches.  The park has a number of well-marked trails offering interesting walks ranging from short pleasant strolls to serious full-day hikes.  Unlike some national parks, visitors to Bako are almost guaranteed to see wildlife. Long-tailed macaque monkeys and silver leaf monkeys are ever present, wild boar are often found rummaging around the park HQ, squirrels and monitor lizards are also common. There is every chance of seeing the rare and unusual proboscis monkeys on trails such as Telok Paku and Telok Delima, particularly if you go late afternoon. You are more likely to see wildlife if you quietly follow the trails and keep listening.<br>Bako also has some good white sand beaches that provide perfect resting spots in between jungle treks. Telok Pandan Kecil is perhaps Bako’s best beach. After an hour or so the Telok Pandan Kecil trail comes to a rocky headland. The view down to a beautiful secluded bay, and the thought of a swim in the cool waters, has a magical effect on tired calf muscles. The following 20 minute descent suddenly becomes more enjoyable. All of the beaches are bordered by limestone and sandstone cliffs, and the action of the sea has left some remarkable rock formations, such as the famous Sea Stack.
                        <br>
                        If you take a stroll at sunset on Telok Assam beach near the park HQ, you will see hundreds of swifts hovering around their nests at the rocks at the far end of the beach. Don’t leave the beach after the sun disappears behind Mount Santubong. Wait another 20 minutes as the sky’s colours often change dramatically, providing a colourful backdrop to the mountain.</p>
                                              
