          <h1>ABOUT SARAWAK</h1>
        <p>
        Sarawak (/səˈrɑːwɒk/; Malay: [saˈrawaʔ]) is a state of Malaysia. The largest among the 13 states, with an area almost equal to that of Peninsular Malaysia, Sarawak is located in northwest Borneo Island, and is bordered by the Malaysian state of Sabah to the northeast, Kalimantan (the Indonesian portion of Borneo) to the south, and Brunei in the north. The capital city, Kuching, is the largest city in Sarawak, the economic centre of the state, and the seat of the Sarawak state government. Other cities and towns in Sarawak include Miri, Sibu, and Bintulu. As of the 2015 census, the population of Sarawak was 2,636,000. Sarawak has an equatorial climate with tropical rainforests and abundant animal and plant species. It has several prominent cave systems at Gunung Mulu National Park. Rajang River is the longest river in Malaysia; Bakun Dam, one of the largest dams in Southeast Asia, is located on one of its tributaries, the Balui River. Mount Murud is the highest point in Sarawak.Because of its natural resources, Sarawak specialises in the export of oil and gas, timber and oil palms, but also possesses strong manufacturing, energy and tourism sectors. It is ethnically, culturally, and linguistically diverse; major ethnic groups including Iban, Malay, Chinese, Melanau, Bidayuh and Orang Ulu. English and Malay are the two official languages of the state; there is no official religion.
        <br><br>
        <span>Weather: </span><p>Sarawak has a tropical geography with an equatorial climate and experiences two monsoon seasons: a northeast monsoon and a southwest monsoon. The northeast monsoon occurs between November and February, bringing heavy rainfall while the southwest monsoon, which occurs between March and October, brings somewhat less rainfall. The climate is stable throughout the year except for the two monsoons, with average daily temperature varying between 23 °C (73 °F) in the morning to 32 °C (90 °F) in the afternoon at coastal areas. Miri has the lowest average temperatures in comparison to other major towns in Sarawak and has the longest daylight hours (more than six hours a day), while other areas receive sunshine for five to six hours a day. Humidity is usually high, exceeding 68 per cent, with annual rainfall varying between 330 centimetres (130 in) and 460 centimetres (180 in) for up to 220 days a year. At highland areas, the temperature can vary from 16 °C (61 °F) to 25 °C (77 °F) during the day and as low as 11 °C (52 °F) during the night</p>
