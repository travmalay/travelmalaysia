                        <p class="para">
                        Kubah National Park covers 2,230 hectares of mixed dipterocarp forest that is rich in palms, orchids and pitcher plants. The park is dominated by the 911 metre high Mount Serapi, the mountain that provides the stunning backdrop to sunsets over the Kuching Waterfront. The forest at Kubah is mixed dipterocarp, with small areas of scrub forest and isolated patches of kerangas or heath forest.

                        Located just 20 km from Kuching, Kubah National Park is a good day trip destination. There is also a range of accommodation at the park HQ for those interested in an overnight stay. The park’s rainforest scenery is superb and it has a rich variety of orchids and ferns. It is also  famed for its rich diversity of palms, with 93 species found in the park. The park’s jungle streams and waterfalls offer great resting places to cool down after a jungle trek.</p>
                                                                      
