                            <span>GETTING THERE </span><br><p>Batang Ai National Park is at the headwaters of the Batang Ai and Lubang Baya rivers, some 15km upriver from the Batang Ai Hydro Lake. Access is by motorized traditional longboat from the Aiman Batang Ai Resort jetty or the Batang Ai public jetty. There is no public longboat service and transport should be arranged in advance. The boat journey is roughly 2 hours – 1 hour crossing they hydro lake and another hour ascending the fast-flowing Batang Ai river. At times of low water visitors may have to jump into the crystal clear stream and help to push the boat through small rapids – all part of the fascinating Batang Ai experience.

                            The Batang Ai hydro lake is about 275km (or 5 hours drive) from Kuching. Most visitors arrive as part of an organised tour group where all transport is included. Contact the Visitor’s Information Centre in Kuching for a list of approved tour operators.</p>
                            
                           
