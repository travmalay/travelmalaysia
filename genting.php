                        <p class="para">
                        The Awana Skyway, also referred to as the new Awana Skyway (circa. 2016), is a gondola lift system connecting Awana Transport Hub, Chin Swee Temple and SkyAvenue in Genting Highlands, Pahang, Malaysia since December 2016. The Awana Transport Hub terminus consists of the new Awana Bus Terminal, the station building and a new 8-storey car park while the other terminus is located at SkyAvenue.

                        The Awana Skyway is one of the two aerial lines serving Genting Highlands, with the Genting Skyway serving as an alternate route. The gondola lift system was launched in 2016, and it replaces the former 2-car aerial tramway system running between 1977 and 2014. The gondola lift system can carry up to 3,600 passengers per hour with 10 passengers per gondola. At the maximum speed of 6 metres per second (21.6 km/h), the 2.8-kilometre journey up the mountain peak takes about 10 minutes, though it varies on the weather conditions.</p>
                       
