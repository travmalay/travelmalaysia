                        <p class="para">
                        The Japanese Village is a series of Japan-themed attractions, spread across a garden landscape on a hill top at Berjaya Hills, Bukit Tinggi. Located five minutes away by car or shuttle ride away from Colmar Tropicale Resort, the village also comprises the Japanese Tea House, Botanical Garden, Tatami Spa, Ume Tatami Suite and Ryo Zan Tei Restaurant.

                        Here, visitors can explore rock pools, koi ponds, flower gardens and a Japanese tea house, which conducts a traditional tea ceremony several times daily. Surrounded by lush rainforest, the village is also a popular spot for bird watching, with views of the valley from strategic points along the gravel trails.</p>
                       
