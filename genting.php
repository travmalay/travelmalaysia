                        The recreation park of the Kedongdong river is located alongside the road from Batang Kali to Gohtong Jaya in the genting Highlands. It is quite basic, with some stalls, a popular place during the weekends for family picnics. The river water is clean and there are beautiful cascades.Not many people know that there is also a nice waterfall upstream of the park, less that one hour trekking away. From the car park there are steps down to the river and you will find that a rather vague trail starts there. Follow this trail or the river bed. Huge boulders make the river trekking interesting but also sometimes difficult.The waterfall comes as a reward with a huge pool. Unspoiled, beautiful jungle environment. You can continue the trail to the top of the fall, where you will find a second, smaller tier.There might be more waterfalls further upstream. We followed the river for another hour, but the trekking became more and more difficult. Maybe the local Orang Asli could help.</p>
                       
