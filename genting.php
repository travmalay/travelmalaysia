                        <p class="para">
                        Chin Swee Caves Temple is a Taoist temple that’s set on a 28-acre plot of rocky forested land in Genting Highlands, where locals often frequent for religious rituals and enjoy the serene atmosphere. It was erected in honour of Reverend Master Chin Swee, a Buddhist monk from the Fujian Province.

                        One of the temple’s most recognised landmarks is a nine-storey Pagoda, which is fitted with images of Buddha and thousands of lamps. Visitors are allowed to climb the steps towards the top of the pagoda, where they are greeted with panoramic views of the mountain. Within the temple grounds are gigantic statues of Buddha and Kuan Yin, as well as intricate sculptures of animals and inscriptions of ancient Chinese traditions and beliefs.</p>
                       
                        <br />
                        <ul>
                        <li class="list-group-item list-group-item-secondary">
                          <span>What to See and Do at Chin Swee Caves Temple </span><p>
                          Chin Swee Caves Temple’s main square also houses several prayer halls for devotees to pray and meditate during their visit, while those looking to learn more about the Buddhism can explore the depiction of 10 Chambers of Hell, which are set along the fringe of the square. While this attraction aims to discourage evildoing by depicting the painful consequences in the afterlife, it’s relatively suitable for young visitors. </p>
                          </li>
                          <li class="list-group-item list-group-item-secondary">
