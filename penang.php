                          <li class="list-group-item list-group-item-secondary">
                          <span>Climate </span><p> The most important feature of Penang Hill is the cooler climate. The core summit area has an average temperature ranging from 20° to 27 °C. The mean minimum temperature is consistently below 21 °C. The hottest months are between December and April while the coolest months are between June and October. It is generally 5 degrees cooler than Georgetown.</p>
