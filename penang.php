                        <p class="para">
                        Standing on a hilltop at Air Itam, near Penang Hill, Kek Lok Si is the largest Buddhist temple in Malaysia. The complex is divided into three zones while the temple grounds comprise the hill entrance, souvenir, food and drinks stalls and the turtle liberation pond. The mid section of the temple houses temples, gardens, the pagoda ; meanwhile the hilltop plays host to an enormous statue of Kuan Yin as well as more gardens and temples.
                        </p>
                       
                        <br />
                        
                        <ul class="list-group para">
                          
                         
                          <li class="list-group-item list-group-item-secondary">
                          <span>OTHER SCENARIO </span><p>The complex is a cornerstone of the Malaysian Chinese community. Also known as the ‘Temple of Supreme Bliss’, it features a maze of souvenir kiosks as well as a turtle and fish pond. The turtle pond – known as The Liberation Pond – was built because according to Chinese tradition, turtles symbolize longevity, strength and endurance and the act of capturing and freeing a turtle is a symbol of spiritual liberation.<br>
                          Additionally, there’s a three-storey shrine with a large Buddha icon that was donated by King Bhumibol of Thailand – you can find several temples here as well as shops and a vegetarian restaurant. At the highest level there is a 36.5 metre-high bronze statue of Kuan Yin; in the future 16 ornately decorated bronze columns supporting a roof over the statue, as well 1000 two-metre high statues are planned to be built.<br>At the apex are a couple more temples, a fish pond, sprawling gardens and 12 animal statues that represent the Chinese zodiac. Kek Lok Si temple houses tablets and imperial Buddhist sutras gifted by Manchu Emperor Kwang Xi and Empress Cixi of the Ching Dynasty; these historic relics are kept in the temple archives. The impressive complex is usually crowded with visitors and is located approximately three kilometres from Penang Hill.</p>
