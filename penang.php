                        <p class="para">
                        The Top is a rooftop viewing deck in George Town, Penang — one of Malaysia’s most beautiful cities. The Komtar tower’s most famous attraction is the Rainbow Skywalk, a transparent glass walkway that lets you watch the streets and lights of Penang way down beneath your feet. But many people don’t know that you can also visit the Komtar building for English afternoon tea,  with the same great views but without the entrance fee! Read on for more information about visiting the Komtar Rainbow Skywalk at The Top, Penang — including what to see and do, how to get there, and how much it costs.
                        </p>
                       
                        <br />
                        
                        <ul class="list-group para">
                          
                         
                          <li class="list-group-item list-group-item-secondary">
                          <span>SCENARIO </span><p>The Komtar Tower is the tallest skyscraper in Penang and at 249 metres high, it’s also the 6th highest building in Malaysia. The tower’s a bit of a jack of all trades. It’s close to George Town’s historic heritage area and most of the city’s main attractions, and it’s a shopping centre, office block, transport hub and shiny tourist attraction in one.

                          At the top of the Komtar Tower has a viewing platform with incredible views of Penang’s city skyline, the blue bay dotted with ships, and the mountains beyond. It’s a great place to have a best views in the city.</p>
