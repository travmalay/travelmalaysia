                        <p class="para">
                        Penang Hill was the first colonial hill station developed in Peninsular Malaysia. Comprising Western Hill, Bukit Laksamana, Tiger Hill, Flagstaff Hill and Government Hill, it is located six km away from Georgetown. The hilly and forested area is the state's primary hill resort.Penang Hill covers a number of hills, with the highest point at Western Hill which is 833 m (2,733 ft) above sea level. The hill stands out prominently from the lowlands as a hilly and forested area. It was used as a retreat during the British colonial period, and is now a popular tourist destination in Penang.

                        The top of the hill is accessible via the Penang Hill Railway from its base station at Hill Railway Station Road. To date, this funicular railway system is the only one of its kind in Malaysia, transporting over a million visitors to the peak of Penang Hill as of 2014.

                        Set 821m above Penang’s capital, islanders call it Bukit Bendera and it is generally about five degrees cooler than Georgetown. It is the last patch of tropical rainforest in Penang so the flora and fauna here have been protected since 1960. It does not have the same prominence as Genting Highlands, Fraser’s Hill or Cameron Highlands but it is one of Penang’s best-known tourist attractions due to its fresher climate.
                        </p>
                       
                        <br />
                        
                        <ul class="list-group para">
                          
                         
                          <li class="list-group-item list-group-item-secondary">
                          <span>Penang Hill History </span><p>Penang Hill was discovered when soon after British settlement; Francis Light commissioned the area to be cleared to grow strawberries. Though it was never fully developed (it was difficult to carve out the forest area), it became a favourite expatriate refuge before the advent of air conditioning. Its oldest bungalow, Bel Retiro, is the holiday residence of the Governor of Penang.

                          Today, the ridge on top of Penang Hill is known as Strawberry Hill.</p>
                          </li>
                          <li class="list-group-item list-group-item-secondary">
                          <span>Views from The Top </span><p>From the top of Penang Hill on a clear day you can see the mountains of Langkawi and north Kedah but it is the night time sight of lit-up Georgetown that is especially rewarding. Most people simply come to enjoy the cool, unpolluted air and walk under 30m-high trees along the nature trails. Additionally, over 100 species of birdlife, ranging from ordinary garden species to exotic deep forest inhabitants, call this hillside home.

                          Two kilometres away from Bellevue Hotel is the Canopy Walk: this 220m walkway suspended between towering trees 30m above the dense jungle offers great views of the hillside.</p>
                          </li>

                          <li class="list-group-item list-group-item-secondary">
                          <span>Penang Hill Railway </span><p> The most popular way to the top of the hill is the Penang Hill Railway. Located at the foot of the hill, this Swiss-designed funicular starts out from Air Itam. Built in 1923, it is one of the world’s oldest funicular systems and has a 2,007m-long track that climbs the hill at a crawling 30-minute pace.

                          The journey takes you past luxuriant greenery and the occasional macaque plus the bungalows originally built for British officials and other wealthy citizens. The queue for carriages can be quite long especially on weekends and public holidays: passengers have to change trains midway.

                          Another way to the top of Penang Hill is by a five km drive up a private road accessible only to the vehicles of hillside residents: this jeep trail is a popular hiking route and starts out from the quarry at the entry of the Penang Botanic Gardens.

                          There is a well-marked eight km path leading up from the Moon Gate (between the post office and police station) at the Botanical Gardens: it is a steep one-hour climb, but a delightful walk with plenty of places to rest along the way.

                          There is a food court, fruit and souvenir stalls, some gardens, an exuberantly decorated Hindu temple, a mosque, a police station, a post office and an 11-room hotel (Bellevue Hotel) at the upper funicular station: the original funicular train, built in 1897, is also on exhibit here.</p>
                          </li>
