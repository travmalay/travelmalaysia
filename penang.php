          <h1>ABOUT PENANG</h1>
        <p>
        Penang is a Malaysian state located on the northwest coast of Peninsular Malaysia, by the Malacca Strait. It has two parts: Penang Island, where the capital city, George Town, is located, and Seberang Perai on the Malay Peninsula. They are connected by Malaysia's two longest road bridges, the Penang Bridge and the Sultan Abdul Halim Muadzam Shah Bridge; the latter is also, as of May 2019, the longest oversea bridge in Southeast Asia. The second smallest Malaysian state by land mass, Penang is bordered by Kedah to the north and the east, and Perak to the south.

        Penang's population stood at nearly 1.767 million as of 2018, while its population density rose to 1,684/km2 (4,360/sq mi). It has among the nation's highest population densities and is one of the country's most urbanised states. Seberang Perai is Malaysia's second-largest city by population.Its heterogeneous population is highly diverse in ethnicity, culture, language and religion. Aside from the three main races, the Malays, Chinese, and Indians, Penang is home to significant Eurasian, Siamese and expatriate communities.George Town is also home to a UNESCO World Heritage Site. A resident of Penang is colloquially known as a Penangite or Penang Lang (庇能儂 Pī-néeng-lâng) in Penang Hokkien.
        <br><br>
        <span>Weather: </span><p>Average temperature 31 degree celcius.As in the rest of Malaysia, Penang has a tropical rainforest climate bordering on a tropical monsoon climate, although the state does experience slightly drier conditions from December to February of the following year. The climate is very much dictated by the surrounding sea and the prevailing wind system.

        Penang's proximity with the island of Sumatra makes it susceptible to dust particles carried by wind from perennial but transient forest fires, creating a phenomenon known as the Southeast Asian haze.</p>
