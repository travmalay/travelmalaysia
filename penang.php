                          <span>The Komtar Rainbow Skywalk at The Top, Penang </span><p>Right on the top of the building, on the 68th floor, is the rooftop restaurant, with a big outdoor area (the restaurant is under cover though). Most famously, you can test your vertigo on the Rainbow Skywalk.

                          The Skywalk is a transparent glass ledge jutting out from the side of the building’s rooftop, where you can walk out over the city and look down at the streets, cars and lights of George Town quite literally beneath your feet.</p>
                          </li>
