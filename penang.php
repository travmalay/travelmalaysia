                          <span>The Window at The Top </span><p>The Window at The Top is on the 65th floor of the Komtar tower. This is an indoor viewing platform observatory deck where you can look out at the city through big floor-to-ceiling windows from inside the building. There’s also a small transparent walkway where part of the floor is made of glass here, so you can look down and see the streets below your feet. The views are fairly spectacular, and although for me the Rainbow Skywalk was definitely the main event, I still think it’s worthwhile checking out the Window at The Top too.</p>
                          </li>
                          <li class="list-group-item list-group-item-secondary">
                          <span>The secret third option: English tea on floor 59 </span><p>This isn’t technically part of The Top’s viewing decks at the Komtar building, but I think it’s just as nice and deserves a mention here. You can go for English afternoon tea on the 59th floor of the same building at The Top View Restaurant and Lounge. It’s not quite as high up and you don’t get to experience being on the rooftop or walking over a transparent glass walkway — BUT the views are still pretty great. And, it’s cheaper to have a delightful afternoon tea with great views (by quite a bit) than to pay for the entrance fee to the Rainbow Skywalk. Full price details are below.<br> Even if you decide you’re not bothered about visiting the Skydeck and viewing platforms, this is a lovely, cool, very calm spot for tea if you need a bit of a relaxing break from sightseeing while you’re in Penang. It’s served every day from 11am to 5pm and in my experience you don’t need to book — just walk up to the Top information desk and they’ll take you up.</p>
                          </li>
                          <li class="list-group-item list-group-item-secondary">
                          <span>When’s the best time to visit </span><p>The Top is an absolutely incredible sunset spot, with epic pink and orange views (on a clear day) over the water, city and mountain peaks. But it also looks stunning at night, when you can watch the city lights come out. I’d suggest arriving just before sunset so you have time to first visit the Window at The Top, before moving on to the Rainbow Skywalk to watch the sunset and then see the lights come on as dusk falls.</p>
                          </li>
