      <h1>ABOUT LANGKAWI</h1>
        <span>ETYMOLOGY: </span><p>
        There are many suggestions for the origin of the name of Langkawi. According to one interpretation, Langkawi means island of the reddish-brown eagle, a Brahminy kite in colloquial Malay. The Malay word for eagle is helang (shortened to lang), and kawi is a red stone used as a chalk to mark goods. This interpretation was used to create the landmark sculpture of an eagle as the symbol of Langkawi at Dataran Helang (Eagle Square) in Kuah<br>
        <span>Weather: </span><p>Langkawi receives more than 2,400 mm (94 in) of rain annually. Langkawi features a Tropical savanna climate (Köppen climate classification (Aw) ) Langkawi has a true dry season from December until February. March to November is a long rainy season. September is the wettest month, when it normally receives more than 500 mm (20 in).</p>

        <span>TOURISM: </span><p>On 1 June 2007, Langkawi Island was given a World Geopark status by UNESCO.Three of its main conservation areas in Langkawi Geopark are Machincang Cambrian Geoforest Park, Kilim Karst Geoforest Park, and Dayang Bunting Marble Geoforest park (Island of the Pregnant Maiden Lake). These three parks are the most popular tourism area within Langkawi Geopark. In 2014, UNESCO issued a "yellow card" warning threatening the status of the Geopark</p>
        </p>

        <span>HOW TO ENTER INTO THE ISLAND: </span><p>Tourists can enter the island via ferry from Kuala Kedah, Kuala Perlis, and Penang. Or by domestic flight from Kuala Lumpur International Airport (KUL) via Malaysia Airlines, AirAsia, Malindo Air, or Subang Sultan Abdul Aziz Shah Airport (SZB) via Malindo Air and Fireflyz providing budget connection to the island. International connection is available from Singapore via Tigerair and AirAsia, as well as from Guangzhou via AirAsia.Always keep in mind that there is no bus or public transportation available in Langkawi.You need to hire cab or car or motorcycle to reach into your destination.</p>
