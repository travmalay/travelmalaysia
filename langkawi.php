                        <p class="para">
                        Known locally as Telaga Tujuh, Seven Wells Waterfall is so named because seven natural pools have been formed at different levels by water streams that flow down the second highest mountain of Langkawi - Mount Mat Cincang. This is the most picturesque and undoubtedly the best waterfall in the island. 
 
 
 
                          The total vertical fall is 91 meters and the lowest pool is formed at the bottom. Seven Wells Waterfall is located in Pantai Kok area and close to the Pantai Kok Beach and Oriental Village where the Cable Car ride takes place. 
                          
                          You need to hike from the car park. There are two ways of watching the waterfall. If you are not too keen climbing and tiring yourself and like to take a quick look of the vertical waterfall, then take a short walk up the service road that leads from the car park and take the left turn to reach the bottom of the falls. You will get great view of the fall from here. If you want, you can even bathe at the natural shallow pool here. 
                          
                          But the real fun is when you reach up to the top of the fall. And nothing comes easy. You can either continue along the steep service road (this time take the right instead) or take the stairs from the car park (behind the stalls) that goes all the way up to the top. There are 638 steps to climb and the total altitude at which you will finally reach the top of the waterfall is about 480 meters. There are signs on the way showing how many steps are remaining and height to climb further. So if you are going to the top, take easy and slow steps. The pathway goes through the rainforest of Mount Mat Cincang where you will hopefully see lots of flora and fauna including bird life. 
                        </p>

                        <span>PRECAUTION: </span><br><p>
                        There are few things to be careful about while at top of Seven Wells Waterfall. Do not stand on the edge of a rock. They can get very slippery at times and you may have a bad fall. The rocks at the bottom of the pools are also quite slippery, so take care while plunging into the pool or getting out of it. Second, do not go beyond the barricade area on top that has prominent warning signs. It is dangerous to go there as several fatal accidents have taken place there. The drop from there is 65 meters into the waterfall and there won't be any chance of survival. 
                        </p>
                       
