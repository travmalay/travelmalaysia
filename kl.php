                        <p class="para">
                        The Klang Gates Quartz Ridge (Malay: Permatang Kuartza Genting Klang) is a quartz dyke that runs through northeast of Kuala Lumpur and the state of Selangor, Malaysia within Bukit Lagong-Kanching-Klang Gates region. With the dimension of more than 14 km long and 200 m wide, it is the longest quartz formation in the world. The other major quartz vein in Kuala Lumpur is along Kajang-Cheras road, which is about 8 km long, however the Klang Gates Quartz Ridge is one-of-its-kind because it has four types of quartz formation. The area has five endemic plants species among 265 species, found nowhere else in the world. A rare animal, the serow is found here as well.

                        Despite being one of the most unusual geological formations in Malaysia, the Klang Gates Quartz Ridge has little conservation and currently being threatened by urban development. More recently, the proposed East Klang Valley Expressway became the latest threat towards the Klang Gates Quartz Ridge, as the expressway alignment may pass through the quartz strip
