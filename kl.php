      <h1>ABOUT KUALA LUMPUR</h1>
        <p>Kuala Lumpur (Malaysian pronunciation: [ˈkualə, -a ˈlumpo(r), -ʊ(r)]), officially the Federal Territory of Kuala Lumpur (Malay: Wilayah Persekutuan Kuala Lumpur) and colloquially referred to as KL, is a federal territory and the capital city of Malaysia. It is the largest city in Malaysia, covering an area of 243 km2 (94 sq mi) with an estimated population of 1.73 million as of 2016.Greater Kuala Lumpur, also known as the Klang Valley, is an urban agglomeration of 7.564 million people as of 2018.It is among the fastest growing metropolitan regions in Southeast Asia, in both population and economic development.Kuala Lumpur has a comprehensive road system supported by an extensive range of public transport networks, such as mass rapid transit (MRT), light rapid transit (LRT), monorail, commuter rail, public buses, hop on & hop off buses (free of charge) and airport rail links. Kuala Lumpur is one of the leading cities in the world for tourism and shopping, being the 6th most-visited city in the world in 2019.The city houses three of the world's 10 largest shopping malls.

        Kuala Lumpur has been ranked by the Economist Intelligence Unit's Global Liveability Ranking at No. 70 in the world, and No. 2 in Southeast Asia after Singapore.Kuala Lumpur was named as World Book Capital 2020 by UNESCO</p>
        
        <p>
          <span>WEATHER: <br></span> Protected by the Titiwangsa Range in the east and Indonesia's Sumatra Island in the west, Kuala Lumpur is safe from strong winds and has a tropical rainforest climate (Köppen climate classification Af), which is hot, humid and sunny, along with abundant rainfall, especially during the northeast monsoon season from October to March. Temperatures tend to remain constant. Maximums hover between 32 and 35 °C (90 and 95 °F) and sometimes hit 38 °C (100.4 °F), while minimums hover between 23.4 and 24.6 °C (74.1 and 76.3 °F) and have never fallen below 17.8 °C (64.0 °F). Kuala Lumpur typically receives minimum 2,600 mm (100 in) of rain annually; June and July are relatively dry, but even then rainfall typically exceeds 131 millimetres (5.2 in) per month.

          Flood is a frequent occurrence in Kuala Lumpur after heavy downpours, especially in the city centre, because the structural irrigation lags behind the intensive development within the city.Smoke from forest fires in nearby Sumatra sometimes casts a haze over the region. It is a major source of pollution in the city together with open burning, emission from motor vehicles and construction.
        </p>
