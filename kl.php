                      Batu Caves in short also referred as 10th Caves or Hill for Lord Muruga as there are six important holy shrines in India and four more in Malaysia. The three others in Malaysia are Kallumalai Temple in Ipoh, Tanneermalai Temple in Penang and Sannasimalai Temple in Malacca.Rising almost 100 m above the ground, the Batu Caves temple complex consists of three main caves and a few smaller ones. The biggest, referred to as Cathedral Cave or Temple Cave, has a very high ceiling and features ornate Hindu shrines. To reach it, visitors must climb a steep flight of 272 steps.The statue height is 42.7 m (140 ft) high, the world's tallest statue of Murugan, a Hindu deity, is located outside Batu Caves. The statue, which cost approximately 24 million rupees, is made of 1550 cubic metres of concrete, 250 tonnes of steel bars and 300 litres of gold paint brought in from neighbouring Thailand.
