                        <p class="para">
                        The Kuala Lumpur Tower (KL Tower; Malay: Menara Kuala Lumpur) is a communications tower located in Kuala Lumpur, Malaysia. Its construction was completed on 1 March 1995. It features an antenna that increases its height to 421 metres (1,381 feet) and is the 7th tallest freestanding tower in the world. The roof of the pod is at 335 metres (1,099 feet). The rest of the tower below has a stairwell and an elevator to reach the upper area, which also contains a revolving restaurant, providing diners with a panoramic view of the city.

                        Races are held annually, where participants race up the stairs to the top. The tower also acts as the Islamic falak observatory to observe the crescent moon which marks the beginning of Muslim month of Ramadhan, Syawal, and Zulhijjah, to celebrate fasting month of Ramadhan, Hari Raya Aidilfitri and Aidiladha. The tower is the highest viewpoint in Kuala Lumpur that is open to the public.Current products and services offered at KL Tower include Observation Deck, Revolving Restaurant (Atmosphere 360°), Mega View Banquet Hall for Meeting and Seminars / Private Functions such as Wedding in the Sky, Mini Theater, Amphitheater, F1 Simulator, Blue Coral Aquarium, KL Tower Mini Zoo and MKL Performing Arts Group.
