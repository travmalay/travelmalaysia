                        <p class="para">
                        Mount Pulai is a 14.3 kilometer moderately trafficked loop trail located near Kulai, Johor, Malaysia that features a waterfall and is rated as difficult. The trail is primarily used for hiking and trail running.
                        </p>
                        <ul class="list-group para">
                          <li class="list-group-item list-group-item-secondary">
                            <p>
                              <span>TRANSPORTATION </span><br>
                              Gunung Pulai is located at Kampung Sri Gunung Pulai, about 12km south-west of Kulai town, or 19km north west from Johor Bahru central. The trail head of Gunung Pulai (a gate) is located about 30-50 meters on the road from the car park at approximately GPS 1.590354, 103.517270 (maps).

                            From the north south highway, take the Exit 252 â€“ Kulaijaya toward Kulaijaya/Kota Tinggi. After exiting the toll, immediately lookout for the signboard for direction to Gunung Pulai â€“ it should direct you to take a right turn immediately. You will be on the road name Jalan Sawah heading south-west. Continue on this route for about 9.3km and at approximately GPS: 1.592808, 103.510175 (maps), turn left into Jln TM Gn Pulai.  Continue straight on the road till you reach the carpark /trailhead.

                            Parking is charged at RM2 for cars, and RM0.50 for motorcycles. As it gets crowded during the weekends, the car park gets full quickly and vehicles are parked by the roadside for free. Take note not to obstruct traffic while parking by the roadside.
                            </p>
                            
                          </li>
                          <li class="list-group-item list-group-item-primary">
                          <span>TRAIL </span><p>The trail head of Gunung Pulai (a gate) is on the main road, about 30-50 meters from the car park. The gates are usually lock to restrict vehicle access. Hikers can bypass the gate on the left.

                            There are two trails leading to the summit for Gunung Pulai. The main road trail and the jungle trail. The main road trail is 5km long on the tarmac road and gets steeper nearing the summit. The jungle trail which is about 3.14km long, split away and mergers back at 1km and 3.9km of the main road respectively.</p>
                            
                          </li>
                          
                          
