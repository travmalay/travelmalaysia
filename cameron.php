                        <p class="para">
                        Butterfly Farm is a tourist attraction at Kea Farm in Brinchang - a live display of butterflies, insects and animals just likeButterfly Garden next door. The complex also houses a cafe, souvenir shop and Chinese steamboat restaurant with a small parking lot to accommodate visitors. Inside the farm, visitors may also explore a flower garden and feed domesticated animals such as rabbits, guinea pigs and ducks.The Butterfly Farm itself is not really that big, it does not have a huge collection of living butterflies, mainly observed the Rajah Brookes (Trogonoptera brookiana albescens), the Malaysian National "Bird" and some smaller species. The interesting things of the farm are a quite good collection of beetles that are on display; they had on display, Cheirotonus arnaudi, Odontolabis femoralis femoralis, and some other interesting species. It is an interesting trip to visit the butterfly farm and the butterfly garden, both are on the same road. It is not too far away from Tanah Rata, about 20 km, and easy accessible via bus or taxi. Entrance fee is very cheap, about one Euro. If you come to the Camerons, check the farm out, it is easier to see Trogonoptera there than anywhere else in the wild. </p>
                       
