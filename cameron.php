                        <p class="para">
                        Big Red Strawberry Farm is a agrotourism park growing hydroponic strawberries and organic vegetables. Perched on a steep hill slope overlooking Brinchang, the center is accessible by road from Star Regency Hotel, marked by large signboards from town. Once at the foothill, the entrance is located right next to Cactus Valley.

                        Starting from the main entrance, visitors will climb up some steps to a koi fountain, flower nursery and mini petting zoo filled mostly with rabbits. The walkway then leads through the strawberry farm where self-picking for purchase is allowed. Finally, the walk ends at a cafe and shop where fresh strawberries and other local products may be purchased.</p>
                       
