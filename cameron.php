                        <p class="para">
                        Boh Tea is the largest producer of tea in Malaysia, with estates in Cameron Highlands and Bukit Cheeding (Selangor). The largests plantation sits at Habu in Ringlet next to its immediate neighbour Fairlie Garden, while Sungai Palas lies about 20km away in Brinchang. At Ringlet, visitors may access Boh via an exit along the main road at right towards Tanah Rata.

                        One of Cameron's favourite tourist attractions, the plantation features a colonial-era processing factory open to public, heritage tea center with shopping gallery and a delightful cafe that serves refreshments. A small hill next door provides a panoramic view point of the valley draped by lush tea fields, if you're willing to brave a steep climb.  </p>
                       
