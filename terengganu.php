                        <p class="para">
                        Tenggol Island is part of a group of islands within the southern regions of the Terengganu Marine Park. Besides Tenggol the group consists of Pulau Nyireh, Tokong Timur, Tokong Burung and Tokong Talang/Tokong Kemudi and Tokong Laut. There are numerous beautiful beaches, and also a few paths that cross the 3 by 2km island; great for some light trekking. Most people visit the Tenggol for its amazing diving possibilities; there are 20 dive sites around the island. Non-divers could enjoy some snorkeling around the island (though Perhentian Island probably is a better choice).

                        "Tenggol Island was cited as “The Best Diving in Peninsular Malaysia” in Asian Diver Magazine Annual Edition."
                        </p>
                       
                        <br />
                        <ul class="list-group para">
                          
                          <li class="list-group-item list-group-item-primary">
                            <span>Diving at Tenggol Island </span><p>Most travelers visit Tenggol for its great dive sites. There are quite a few very nice sites, though some are only accessible by advanced (or experienced) divers as most sites are affected by strong currents. Beginners are probably better off at nearby islands like Perhentian, Redang or Tioman. There are three dive operators on the island; Discovery Divers, MrDive Malaysia and Scuba Explorers. Most popular dive spots are; Batu Chanang, Tokong Laut (also known as the Highway), Tokong Burung and Coral Garden. There are sites with some nice wrecks and sites where you can enjoy macro underwater life (especially many nudibranches). Whale sharks are seen at certain sites between August and October every year.
                           </p>
                            
                          </li>
                          <li class="list-group-item list-group-item-secondary">
                          <span>Best time to visit Tenggol </span><p>During monsoon season, between October and February, Tenggol Island is closed to visitors. Most of the resort staff temporary move to the mainland to escape the severe weather conditions during these months.</p>
                          </li>
                          <li class="list-group-item list-group-item-secondary">
                          <span>Accommodation at Tenggol Island </span><p>There are only a few resorts on Tenggol Island; Tenggol Resort, Tenggol Island Beach Resort and Tenggol Aqua Resort (also known as Tenggol Island Resort). If you stay at one of these resorts, there is no easy way to get out to the other resorts except by boat. Guests usually have their meals at the resort, as there are no public places with restaurants and such on the island. Typical accommodation at these resorts are traditional (style) wooden cabana’s facing the beach.

                          If you want to stay at a nice place before or after visiting Tenggol, then you might want to consider the following three great 5 star resorts; Tanjong Jara Resort (Dungun), Ri-Yaz Heritage Marina Resort (Kuala Terengganu) and the Aryani Resort (Kuala Terengganu). If you stay at Dungun it is also possible to do a day-trip to Tenggol Island.</p>
                          </li>
                          <li class="list-group-item list-group-item-primary">
                          <span>No ATMs at the island </span><p>There are no ATMs on the island. Some resorts accept credit cards, but it is best to have enough cash with you to cover your expenses while at the island. There is an ATM in Kuala Dungun and at Kuala Terengganu Airport. There are no shops at Tenggol Island, so bring along everything you need from the mainland.</p>
                          </li>
                          <li class="list-group-item list-group-item-secondary">
                          <span>How to get to Tenggol Island </span><p>There are daily flights operated by Malaysia Airlines and AirAsia from Kuala Lumpur International Aiport (KLIA & LCCT) to Kuala Terengganu. From here you take a public bus or taxi to the small coastal town of Kuala Dungun (travel time around 1 hour). Expect to pay only a few ringgits by bus and around RM60 by taxi. At Kuala Dungun jetty you can arrange a boat to Tenggol Island. Tenggol Island is located about 25km from the coast, so it is a 50 minute boat ride to reach your resort.

                          Another option is a flight by Firefly from Subang Airport (nearer to KL compared to KLIA/LCCT) to the town of Kerteh. From Kerteh it is only a 20 minute drive by taxi to Kuala Dungun. This option saves a lot of time when you are traveling from Kuala Lumpur.</p>
                          </li>
                         
                        </ul>
