                        <p class="para">
                        Chamerong waterfall is the highest waterfall in Malaysia.This beautiful serene waterfall exhibits the beautiful sights of water gushing down and pounding the rocks at the height of 1214 metres. Huge, uneven boulders are overlapping on one another, making the drizzles flow in between the rocky cracks - creating a splendid view before your eyes. Chemerong Waterfall's magnificent height makes the water looks like it showers down directly from the sky - a majestic view that makes the two-hour trekking a worthwhile experience.

                        Usually, the two-hour hiking to unravel the hidden waterfall is not enough to satisfy advanced hikers' thirst for adventure. Most of the hikers will opt for an adventurous hiking expedition that might take up to three days and two nights. Activities include pitching tents at provided campgrounds, ascending a 200-metre-tall mountain, relaxing at Jeram Lesung Pool, swimming in a 'fish spa', and many more.
