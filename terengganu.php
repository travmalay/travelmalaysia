                        <p class="para">
                        One of Terengganu's newer attractions is the Islamic Civilization Park (Taman Tamadun Islam) featuring scaled-down replicas of 22 famous monuments from the Islamic world.

                        It was opened in 2008 on an island in the Terengganu River just 1.2km from the Terengganu State Museum on the outskirts of Kuala Terengganu city.

