                        <p class="para">
                        What comes to mind when you think about island getaways? Perhaps Redang Island, Tioman Island, Langkawi Island…but have you ever heard about Lang Tengah Island or Pulau Lang Tengah? If you haven’t, now you do!

                        With more discoveries by tourists over the years and article coverage about the island, Lang Tengah Island’s beauty is slowly coming into the limelight. In fact, this island that is situated off the coast of Terengganu between Redang Island and Perhentian Island is a hidden gem amongst the popular ones in Malaysia. 

                        With white sandy beaches, clear blue waters, wonderful marine biodiversity, Lang Tengah Island has basically everything that makes up a pretty great island for vacation.
                        </p>
                        <ul class="list-group para">
                          <li class="list-group-item list-group-item-secondary">
                            <p>
                              <span>How To Get To Lang Tengah Island Malaysia </span><br>
                              If you are travelling from KL, you can either go by car which is a 4 and a half hour ride and have your car parked at Merang Jetty for RM10 per night. You could also take a bus or train to the city of Kuala Terengganu to reach Merang Jetty if you do not mind travelling for about 7 to 8 hours with stopovers. 

                              Alternatively, you could travel by plane on domestic flights to Kuala Terengganu. The Lang Tengah Island resorts usually will provide a land transport service to pick you up from the airport to Merang Jetty.

                              At Merang Jetty, there are ferry or boat transfers to bring you Lang Tengah Island which operates three times per day. Each ride takes between 40 to 45 minutes.
                            </p>
                            
                          </li>
                          <li class="list-group-item list-group-item-primary">
                          <span>What to Do on Lang Tengah Island Malaysia </span>
                            
                          </li>
                          <li class="list-group-item list-group-item-primary">
                          <span>Scuba Diving & Snorkelling </span><br><p>What is an island holiday without a dip into the waters? You should not miss the opportunity to dive and snorkel at Pulau Lang Tengah, especially when the island is constantly praised for its beautiful diving and snorkelling sites. You can snorkel on your own just off the beach if you are not up for booking a day’s snorkelling trip.

                          Mix around with schools of parrot fish, baby sharks, blue-ringed angelfish, clown fish and more together with large collections of colourful corals and sea anemones. If you want to have guided snorkelling and diving tours, each Lang Tengah Island resort has a dive centre offering various diving and snorkelling packages plus canoe and kayak rentals if you are up for some boat rowing activities. </p>
                            
                          </li>
                          <li class="list-group-item list-group-item-primary">
                          <span>Blue Tears Tour </span><br><p>This is a popular attraction amongst the tourists on Lang Tengah Island! The term “Blue Tears” refers to the blue-dotted sand along the beaches at night, in which you will be able to witness an incredible view of bioluminescent ostracods that look like shrimps of the size of plankton glowing bright blue on the sand. Most of these creatures use the blue light as a defence mechanism while some use the light to mate.

                          However, the Blue Tears event is not available all the time – this occurrence is subject to weather conditions of the island as unsuitable weather will affect the presence of the ostracods.

                          </p>
                            
                          </li>
                          <li class="list-group-item list-group-item-primary">
                          <span>Jungle Trekking </span><br><p>If you are up for some long walks in the jungle, you could jungle trek to Batu Kuching as it is a great place to capture gorgeous sunrise and sunset from the rocks. Depending on where you stay on the island, travelling to Batu Kuching may take half an hour or more. 

                          There are yellow arrow marks on trees and rocks on the trek to guide you to Batu Kuching. Bring along a torchlight, drinking water and wear proper footwear when jungle trekking. Batu Kuching has a lovely snorkelling site so consider bringing snorkelling gears if you plan to go for a swim.

                          If Batu Kuching is not challenging enough for you, you could also go for a more adventurous trek to Batu Bulan, where you can see fish and turtles when you go snorkelling there. </p>
                            
                          </li>
                          <li class="list-group-item list-group-item-primary">
                          <span>Island Hopping </span><br><p>Lang Tengah Island is in the vicinity of Pulau Redang and Pulau Perhentian so if you want a more happening place for a change, you can consider hopping over to the two more popular islands via boat rides. Such rides are available at Lang Tengah Island resorts – just check with the resort you are staying at for more details on the type of boat ride packages they have to offer. </p>
                            
                          </li>
                          <li class="list-group-item list-group-item-primary">
                          <span>Check out the Turtle Conservatory at Turtle Bay </span><br><p>Lang Tengah Island also houses a Turtle Conservatory at Turtle Bay, a more secluded beach among the other beaches of the island. Turtle Bay is the place where turtle conservation and research efforts are carried out by a team of volunteers called Lang Tengah Turtle Watch (LTTW), and also serves as the main nesting area for turtles.

                          If you happen to visit at a time where the turtles come to the ground, you will be lucky to see nests and maybe even some hatchlings depending on the time of the year!  </p>
                            
                          </li>
                          
                          
                        </ul>
