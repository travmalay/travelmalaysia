                        <p class="para">
                        Adrenaline junkies will get a hit at this experience in Terengganu. The hilltop of Bukit Maras is flat and wide, lending the perfect terrain for paragliders to use as a runway. The best part? You can join in on the fun too!

                        Don’t worry, you won’t need a license of any sort for tandem paragliding as you will be accompanied by an experienced pilot. Aside from a few very simple attractions during take-off and landing, there’s nothing else you’ll need to do except marvel at the sweeping views below (and hang on for dear life!). Everything else – from the steering and controls – will be managed by the pilot harnessed safely to you.We get that some of you may have the jitters just thinking about paragliding. But trust that you’ll be in safe hands all the way through. A 1 hour briefing and preparation will be conducted prior to your flight, by the end of which you will be ready for the full 10 minutes of paragliding.

                        Once you’ve taken that leap of faith: your rewards are majestic views of the surrounding landscape. You will sail over one hill peak to another, sweeping over jungles that lead right up to crystal seas.

                        Around you are the surrounding townships of Bukit Maras. You can bet there will be residents looking up towards you as you streak across the skies!
