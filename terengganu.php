                            <span>TRANSPORTATION </span><p>Ferries to both Perhentian islands leave almost every hour from Kuala Besut during regular business hours. Travellers from Kuala Lumpur or other destinations can fly, train, or bus to either Kuala Terengganu or Kota Bharu. From either town, the jetty port in Kuala Besut is accessible by taxi. It is also possible to use minivan services offered by various companies if travelling from Taman Negara, the Cameron Highlands, Kuala Lumpur or many other cities in Malaysia. The minivan services vary in prices and there are many different companies that offer service to and from various destinations. A ferry ride of approximately forty minutes can be arranged by one of the many jetty operators, or in cooperation with many of the resorts. Once on the island, there are limited trails that connect various resorts. A trail on Besar goes through the thick jungle, and some stretches of beach are home to various resorts so visitors can wander freely. The easiest way to travel around the islands is by water taxi, which can be arranged at most resorts.
