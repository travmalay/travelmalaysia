                        <p class="para">
                        Tasik Kenyir (Lake Kenyir) in Terengganu is the largest man-made lake in the country. According to Tourism Malaysia, it spans 209,199ha. It was formed from 1978 to 1985 by damming the Kenyir River.

                        The dam produces hydro-electric power and helps control flooding in the state.

                        The valleys and plains behind the dam filled up with water and the hilltops and uplands remained to form islands, some 340 of them.

                        River fish soon adapted to their new environment and today this freshwater lake has become a challenging spot for anglers to try their skill.

                        The water level goes up and down according to the seasons – the lake is at its fullest and most beautiful during the March and April wet season. Anglers prefer the dry season around August.
                        </p>
                       
                        <br />
                        <ul class="list-group para">
                          
                          <li class="list-group-item list-group-item-primary">
                            <span>THINGS TO DO AT TASIK KENYIR </span>
                          </li>
                          <li class="list-group-item list-group-item-secondary">
                          <span>Boat trips: </span><p>The only way to access many of the sites is by boat, either by chartering one at Pengkalan Gawi jetty or joining an organised tour.</p>
                          </li>
                          <li class="list-group-item list-group-item-secondary">
                          <span>Houseboat:  </span><p>There are a number of houseboats that provide sleep-on-board accommodation while touring the lake.

                          They tend to cater for large groups so a couple or small family would have to pay a lot to book the whole houseboat.Kayaking, canoeing and rafting are other water activities available here. There is probably not enough wind for sailing.</p>
                          </li>
                          <li class="list-group-item list-group-item-secondary">
                          <span>Jungle walks: </span><p>This is an exciting destination for eco-adventurers who want to experience the dense forest and wildlife.

                          It is supposedly possible to access Taman Negara from here via the Tanjung Mentong entryway, but a knowledgeable guide would be required.</p>
                          </li>
                          <li class="list-group-item list-group-item-secondary">
                          <span>Caving: </span><p>There are a number of caves in the limestone formations surrounding the lake.

                          The most notable is Gua Bewah, where Neolithic-age items such as axes, tools and utensils have been found. Nearby Gua Taat has some impressive stalactites.</p>
                          </li>
                          <li class="list-group-item list-group-item-secondary">
                          <span>Waterfalls:  </span><p>There are some 14 waterfalls feeding the lake. Lasir Waterfall, located about 16km from Pengkalan Gawi, has an impressive drop and is a popular spot for boat tours to visit.</p>
                          </li>
                          <li class="list-group-item list-group-item-secondary">
                          <span>Fishing: </span><p>Tasik Kenyir is one of the country’s top inland fishing locations. The ferocious Giant Snakehead is the top predator in these waters. Anglers should note that fishing is only allowed in certain areas.</p>
                          </li>
                          <li class="list-group-item list-group-item-secondary">
                          <span>Birdwatching:  </span><p>It is believed this area is home to about 350 species of birds, including nine out of 10 of Malaysia’s hornbill species.</p>
                          </li>
                          
                         
                          <img src="image/elephant.jpg" class="img-fluid" alt="Responsive image">
                          <li class="list-group-item list-group-item-secondary">
                          <span><a href="https://lilyrianitravelholic.blogspot.com/2015/06/remember-me-i-am-traveler-that-loves-zoo.html">Kenyir Elephant Conservation Village: </a></span><p>Here you can ride, bathe and observe a herd of 13 rescued elephants, just minutes away from Pengkalan Gawi. Check the website for more information.</p>
                          </li>
                          <li class="list-group-item list-group-item-secondary">
                          <span>Kenyir Water Park:  </span><p>This is a small water theme park with a swimming pool, slides, children’s pool and a floating inflatable obstacle course. It is open from 10am to 5:30pm. Adults pay RM25 and children, RM15. The price includes boat transport.</p>
                          </li>
                          <li class="list-group-item list-group-item-secondary">
                          <span>Tropical Park: </span><p>Located on Pulau Tekak Besar, Pulau Sungai Tekak and Pulau Anak Tekak, about 15 minutes by boat from Pengkalan Gawi, this park boasts 1,170 trees from 110 rare and exotic fruit species such as the Terengganu Cherry, Asam Gelugor and Bacang.</p>
                          </li>
                          <li class="list-group-item list-group-item-secondary">
                          <span>Orchid Park: </span><p>Located on Pulau Hilir Selimbar, Pulau Hulu Selimbar and Pulau Belit, which are linked by hanging bridges. A 10-minute boat ride from Pengkalan Gawi, this park has a wide selection of forest orchids and orchid hybrids, some native to Malaysia and some imported.</p>
                          </li>
                          <li class="list-group-item list-group-item-secondary">
                          <span>Bird Park: </span><p>A seven-minute boat ride from Pengkalan Gawi on Pulau Terengganu Mati. Enjoy peacocks, swans, geese and other species.</p>
                          </li>
                          <li class="list-group-item list-group-item-secondary">
                          <span>Herb Garden: </span><p>Located on Pulau Sah Kecil, 7km from Pengkalan Gawi, this park has 230 species of herbs such as Tongkat Ali, Kacip Fatimah, Cats’ Whiskers and so on. There is an interpretive centre and a reflexology path.</p>
                          </li>
                          <li class="list-group-item list-group-item-secondary">
                          <span><a href="https://www.gayatravel.com.my/5-experiences-for-lake-kenyir-newbies-to-try-out/">Kelah Fish Sanctuary: </span></a><p>Malaysian Mahseer fish are raised here. You can swim with them for an extreme fish spa (they look more like piranhas). There is a suspension bridge and treks are available.</p>
                          </li>
                          
                          <li class="list-group-item list-group-item-secondary">
                          <span>Butterfly Park: </span><p>Located on Pulau Lubuk Geras, five minutes by boat from Pengkalan Gawi.</p>
                          </li>
                          <li class="list-group-item list-group-item-secondary">
                          <span>Tip:  </span><p>Your first stop on arrival should be the Kenyir Tourist Information Centre at Pengkalan Gawi, where you can obtain information on all the attractions. Check their official facebook page for more details</p>
