                        <p class="para">
                        The Maritime Museum or the Flora de Lamar is a replica of the Portuguese ship which sank in the coast of Malacca while on its way to Portugal. The replica is a staggering 34 meters in height and 8 meters in width. The main focus of the museum is the maritime history of Malacca and the golden ages of Malacca’s Sultanate as the Emporium of the East. There are also paintings which displays how the Straits of Malacca was a strategic location for traders from both the east and the west stop by at Malacca/Melaka and conduct their businesses while waiting for the monsoon winds to change direction. The museum has myriads of exhibits of articles such as porcelain, silk, textile and spices used by the seamen of the yesteryears and even come sunken treasures hauled from Diana, another shipwreck. Other than exhibits on the history of the Malaccan Maritime, the museum also showcases the different eras that Malacca has gone through starting from the Malacca Sultanate era to the Portuguese era, Dutch era and ending with the British era.For ship fanatics, there are also a collection of different ship models that are on display at the museum. You will be able to learn more about the types of ships and their functions from the collection. You can also climb up to the upper deck of the ship to view the diorama of the captain cabin.</p>
                                              
