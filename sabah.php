                        <p class="para">
                        Famed for its beauty even before Sipadan was discovered, Pulau Sibuan is a tear-shaped sand cay situated about 30 minutes from Semporna.
                        Sibuan Island (Local Name: Pulau Sibuan) is also known as Battleship Island (军舰岛 in Chinese) because of its long and narrow shape. Its “bow” is the long strip of white sandy beach pointing to the north, and the dense trees behind looks like the bridge of a ship. Covering an area of approximately 15.13 hectares (0.15 square Kilometre), Sibuan is a sand cays island built entirely from the eroded remains of coral reef.
                        Located next to Church Reef, it is known as one of the most picturesque islands in Tun Sakaran Marine Park, due to its largely untouched conditions.

                        Whether you choose to come here for snorkeling, diving or a picnic, you’ll find that Sibuan Island is truly hard to beat as far as beauty and underwater splendor is concerned.


                                            
                        </p>
                       
