                        <p class="para">
                        Mabul is also renowned for its amazing array of macrolife, making it an underwater photographer’s dream location to capture some of the rarest ecological species on film. Flamboyant cuttlefish, blue-ringed octopus, spike-fin gobies, frogfish and moray eels are just some of the spectacular critters you will encounter beneath the waters of Mabul.

                        The island is a small oval shaped island fringed by sandy beaches and perched on the northwest corner of a larger 200-hectare reef. The reef is on the edge of the continental shelf and the seabed surrounding the reef slopes out to 25–30m deep. In addition, Mabul is also a local island-village. Accommodation ranges from backpacker styled lodges to water bungalows.

                        Although all the excitement is underwater, you can always set your scuba gear aside for a day to laze on Mabul’s soft sand to work on your tan. It is about a 45 minute boat ride from the Semporna town jetty.

                        Within a short distance and prominently visible from the island is the Seaventures Dive Rig. The rig was previously used in the oil and gas industry and was decommissioned in 1985 and stationed in Singapore. It was move to Mabul in 1997 and besides housing travelers, the six legs of the rig shelters marine life.
                        </p>
                       
                        <br />
                        <ul class="list-group para">
                        <li class="list-group-item list-group-item-secondary">
                          <span>Things to do </span><p>Since water is a way of life on the island, popular lagoon excursions include snorkeling, diving, cruising, fishing, visiting the Bajau Laut settlements in Tun Sakaran Marine Park, and of course, turtle nesting and hatching.

                         </p>
                          </li>
                          <li class="list-group-item list-group-item-secondary">
                          <span>DIVING MABUL </span><p>Mabul's reef is on the edge of the continental shelf and the seabed surrounding the reef slopes out to a depth of 25 to 30 m. There are several dive resorts operating on Mabul island.

                          Mabul is arguably one of the richest single destinations for exotic small marine life anywhere in the world.[citation needed] Flamboyant cuttlefish, blue-ringed octopus, mimic octopus and bobtail squids are just a few of the numerous types of cephalopods to be found on Mabul's reef. Many types of gobies can be found including the spike-fin goby, black sail-fin goby and metallic shrimp goby. Frogfish are everywhere; giant, painted and clown frogfish are regularly seen along with most of the scorpion fish family.

                          There are six resorts here providing accommodation for scuba divers—most located on the island or on stilts over the water, while one is on a converted oil platform about 500 metres from the beach. There are also several homestay & backpacker accommodations which can arrange diving. All of the resorts/homestays can arrange a day trip to the nearby island of Pulau Sipadan. There is a permit system for Sipadan and all permits may be booked months in advance.</p>
                         </li>
                         
                         </ul>
                         
