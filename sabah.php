          <h1>ABOUT SABAH</h1>
        <p>
        Sabah (Malay pronunciation: [saˈbah]) is a state of Malaysia located on the northern portion of Borneo. Sabah has land borders with the Malaysian state of Sarawak to the southwest and Indonesia's Kalimantan region to the south. The Federal Territory of Labuan is an island just off the Sabah coast. Sabah shares maritime borders with Vietnam to the west and the Philippines to the north and east. Kota Kinabalu is the state capital city, the economic centre of the state and the seat of the Sabah state government. Other major towns in Sabah include Sandakan and Tawau. As of the 2015 census in Malaysia, the state's population is 3,543,500. Sabah has an equatorial climate with tropical rainforests and abundant animal and plant species. The state has long mountain ranges on the west side which form part of the Crocker Range National Park. Kinabatangan River, second longest river in Malaysia runs through Sabah and Mount Kinabalu is the highest point of Sabah as well as of Malaysia.Sabah exhibits notable diversity in ethnicity, culture and language. The head of state is the Governor, also known as the Yang di-Pertua Negeri, while the head of government is the Chief Minister. The government system is closely modelled on the Westminster parliamentary system and has one of the earliest state legislature systems in Malaysia. Sabah is divided into five administrative divisions and 27 districts. Malay is the official language of the state; and Islam is the state religion, but other religions may be practised in peace and harmony in any part of the state.Sabah is known for its traditional musical instrument, the sompoton.

Sabah has abundant natural resources, and its economy is strongly export oriented. Its primary exports include oil, gas, timber and palm oil. The other major industries are agriculture and ecotourism.</p>
        <br><br>
        <span>Weather: </span><p>The land of Sabah is located in a tropical geography with equatorial climate. It experiences two monsoon seasons of northeast and southwest. The northeast monsoon occurs from November to March with heavy rains, while the southwest monsoon prevails from May to September with less rainfall.It also received two inter-monsoon season from April to May and September to October. The average daily temperature varies from 27 °C (81 °F) to 34 °C (93 °F), with a considerable amount of rain from 1,800 millimetres to 4,000 millimetres.The coastal areas occasionally experience severe storms as the state is situated south of the typhoon belt.Due to its location is very close to the typhoon belt, Sabah experience the worst Tropical Storm Greg on 25 December 1996.The storm left more than 100 people dead, with another 200–300 missing, 3,000–4,000 people left homeless</p>
