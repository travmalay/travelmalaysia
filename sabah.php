                        <p class="para">
                        Mantabuan Island is the smallest island in Tun Sakaran Marine Park, with an area of ~10 hectares or 0.1km².

                        It is located just ±2km from the northern coasts of Bohey Dulang and Bodgaya, the biggest island in the marine park.Mantabuan is a volcanically formed and mostly submerged island that is paradise for divers who want to see magnificent coral and with unsurpassed visibility. It is also one of the best islands in the area for manta rays, and devil rays can often be found in the blue as well. The Black Coral Garden, this rare and precious coral can be found in just 22 meter of waters. In addition to the black coral, pristine soft coral and large table coral can be found throughout the entirety of the island. Huge coral boulders in the shallow provide shelter to a large number of marine lives, including myriad nudibranches, painted lobster, blue-spotted ribbon rays, and thousand upon thousand of reef fish. Dive sites further to the north and west of the island on the other hand are great to look for turtles, barracudas and a wide variety of smaller critters.

                        Pulau Mantabuan is inhabited by a small, friendly community of Sea Gypsies (Bajau Laut), who live in houseboats or stilt houses built over coral reefs. Over here, it is not uncommon for visitors to be invited into their homes for a fresh coconut or two.

                        The Malaysian army operates a military base on the island, and if you’re lucky, you may get the chance to snap a photo inside the army outpost that comes complete with artillery and sandbags.

                        There are no motor vehicles or fancy commercial developments here, and visitors to the island are far and few in between.


                                            
                        </p>
                       
