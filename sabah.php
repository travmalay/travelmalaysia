                          Pulau Sibuan’s crystal clear waters are a sight for sore eyes in any beachgoer’s book, making it truly a snorkeling haven that is second to none.

                          Several dive operators organize their PADI Open Water diving courses here, including Scuba Junkie and Sipadan Scuba.<br>

                          <span>Lunch picnics</span><br>
                          The large sandbank over here is a favourite spot for lunchtime picnics.

                          <span>Bajau Laut (Sea Gypsies) village</span>
                          There is a small, friendly community of Bajau Laut living in and around Pulau Sibuan.Sea Bajau are naturally born with a special ability because they have a body part bigger than ours (Want to guess what it is?). They can free dive up to a depth of 200 feet and hold their breath for 13 minutes. They are sea people for centuries, so their spleen evolved to be 50% bigger, to supply more oxygen. They are becoming a mermaid!</p>
                          </li>
                          
                          <li class="list-group-item list-group-item-secondary">
                          <span>GETTING THERE </span><p>To visit Sibuan Island, first fly to Kuala Lumpur or Kota Kinabalu city (capital of Malaysia and Sabah State respectively), then take a direct flight from these cities to Tawau City in East Coast of Sabah by AirAsia or Malaysia Airlines. Then get a taxi from Tawau airport to go to Semporna town, which takes about an hour. From Semporna town, the boat to Sibuan Island takes around 30 minutes.Sibuan is located ±30 minutes by boat from Semporna, ±25 minutes from Pom Pom, ±35 minutes from Mataking and ±1.5 hours from Mabul.
                          </p>
