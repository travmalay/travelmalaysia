                        <ul class="list-group para">
                          
                         
                          <li class="list-group-item list-group-item-secondary">
                          <span>TRAILS </span><p>Now there are 2 trails to the summit, Ranau Trail and Kota Belud trail. The trails will take climbers from Panar Laban (previously known as Laban Rata) at 3,272m to Sayat Sayat (3,668m). And then continue on to the summit trail to Low’s Peak at 4,095.2m. (Note: Both Kota Belud trail and Ranau are new routes to Sayat Sayat, the route from Sayat Sayat to summit is the same route used previously). The new trails may be more challenging than the previous trail, but climbers will be rewarded with breathtaking views along the way.

                          </p>
                          </li>
                          <li class="list-group-item list-group-item-secondary">
                          <span>TEMPERATURE </span><p>The temperature of Mount Kinabalu usually stands from −4 to 8 °C (25 to 46 °F) from December to January, and 3 to 12 °C (37 to 54 °F) from June to September. Due to the coldness of the mountain from December to January, sometimes, frost and ice appear at the summit of Mount Kinabalu.Snow has been found on the topmost peak of the mountain, and it is estimated that it snows on the mountain once every year according to studies.</p>
                          </li>
                          <li class="list-group-item list-group-item-secondary">
                          <span>WHAT ELSE TO DO </span><p> Surrounding the Kinabalu Park is a multitude of natural attractions and enjoyable activities such as mountain biking, golf, birding, farm tours, canopy walk and of course photography!.A variety of accommodation is available within the Park and in its surrounding areas, ranging from basic hostels to luxury chalets.</p>
                          </li>
                          
                          <li class="list-group-item list-group-item-secondary">
                          <span>Essentials </span><p>A good vacation is never without some must-have essentials. As Kinabalu Park is located in a hilly area, it is best to pack up warm clothing and gloves to ward off the cold. Also, don’t forget to bring a highly water resistant knapsack and a raincoat in case it rains.

                          The rough terrains of Mount Kinabalu require its ambitious to-be conquerors to have comfortable walking/ tracking shoes with them. Last but not least, be well prepared with energy snacks e.g. chocolate, nuts, glucose sweets and a water bottle for fuel and hydration. As a safety recommendation, bring along a torch light in case one gets lost at night.</p>
                          </li>
                          <li class="list-group-item list-group-item-secondary">
                          <span>Important notice </span><p> Mt. Kinabalu is a popular destination, visitors who wish to climb Mount Kinabalu are advised to make reservations 6 months in advance to avoid dissappointment. The number of climbers is limited to 135pax daily.</p>
